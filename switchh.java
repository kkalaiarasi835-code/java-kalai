import java.util.Scanner;
class switchh{
    public static void main(String args[])
    {
        int days=5;
        switch(days){
            case 1:
                System.out.println("Sunday");
                break;
            case 2:
                System.out.println("monday");
                break;
            case 3:
                System.out.println("tuesday");
                break;
            default:
                System.out.println("not a valid day");
                break;
        }
        }

    }

